package com.example.javaproject2.javaproject2.week2;

public class VariableEx {
    public static void main(String[] args) {
        String sOne = "1";
        int iOne = 1;
        float fOne = 1.0f; //실수를 대입할 때 f을 붙이지 않으면 double형으로 인식

        System.out.printf("String:%s Number:%d Float:%f", sOne, iOne, fOne);

    }
}
