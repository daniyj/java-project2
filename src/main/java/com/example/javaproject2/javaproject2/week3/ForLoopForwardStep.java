package com.example.javaproject2.javaproject2.week3;

public class ForLoopForwardStep {
    public static void main(String[] args) {
        //짝수 출력
        for(int i = 0;i < 10;i +=2){
            System.out.println(i);
        }
        //홀수 출력
        for(int i = 1;i < 10;i+=2){
            System.out.println(i);
        }
    }
}
